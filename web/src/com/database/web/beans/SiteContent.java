package com.database.web.beans;

public class SiteContent {

	private String defaultPage;
	
	private String searchPage;
	
	private String clientsPage;
	
	private String officesPage;
	
	private String descriptorPage;
	
	private String aboutPage;
	
	private String createPage;

	public String getDefaultPage() {
		return defaultPage;
	}

	public void setDefaultPage(String defaultPage) {
		this.defaultPage = defaultPage;
	}

	public String getSearchPage() {
		return searchPage;
	}

	public void setSearchPage(String searchPage) {
		this.searchPage = searchPage;
	}

	public String getClientsPage() {
		return clientsPage;
	}

	public void setClientsPage(String clientsPage) {
		this.clientsPage = clientsPage;
	}

	public String getOfficesPage() {
		return officesPage;
	}

	public void setOfficesPage(String officesPage) {
		this.officesPage = officesPage;
	}

	public String getDescriptorPage() {
		return descriptorPage;
	}

	public void setDescriptorPage(String descriptorPage) {
		this.descriptorPage = descriptorPage;
	}

	public String getAboutPage() {
		return aboutPage;
	}

	public void setAboutPage(String aboutPage) {
		this.aboutPage = aboutPage;
	}

	public String getCreatePage() {
		return createPage;
	}

	public void setCreatePage(String createPage) {
		this.createPage = createPage;
	}
	
}
