CREATE OR REPLACE PROCEDURE ADD_USER(
  LOGIN VARCHAR2, 
  HASH_PASSWD VARCHAR2, 
  ROLE VARCHAR2, 
  ID_PEOPLE INTEGER,
  IDENT OUT INTEGER
) IS 
PRAGMA AUTONOMOUS_TRANSACTION;
BEGIN 
	INSERT INTO USERS(LOGIN, HASH_PASSWD, ROLE, ID_PEOPLE)
	VALUES (LOGIN, HASH_PASSWD, ROLE, ID_PEOPLE)
  RETURNING ID INTO IDENT;
  COMMIT;
END;
